    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String op = jcbOPcoes.getSelectedItem().toString();
        String search = jtfPesquisa.getText();
        despesaVariavelList.clear();
        if (search.isEmpty()) {
            despesaVariavelList.addAll(facade.findAll());
            jtfPesquisa.requestFocus();
        } else {
            switch (op) {
                case "Descrição": {
                    despesaVariavelList.addAll(facade.findByDescription(search));
                    break;
                }
                case "Valor": {
                    try {
                        double val = Double.parseDouble(search);
                        despesaVariavelList.addAll(facade.findByValue(val));
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Erro a válidação de valor!");
                    }
                    break;
                }
                case "Status": {
                    despesaVariavelList.addAll(facade.findByStatus(search));
                    break;
                }
                case "Vencimento": {
                    Date date;
                    try {
                        date = new SimpleDateFormat("dd/MM/yyyy").parse(search);
                        despesaVariavelList.addAll(facade.findByDatePayment(date));
                    } catch (Exception e) {
                    }
                    break;
                }
            }
            jtfPesquisa.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


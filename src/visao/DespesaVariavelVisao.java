    private void jbtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGravarActionPerformed
        if (despesa == null) {
            despesa = despesaVariavelList.get(jtDespesaVariavel.getSelectedRow());
            facade.updateDatas(despesa);
            JOptionPane.showMessageDialog(null, "Registro atualizado com sucesso !");
            updateTable();
            enableComponents(false);
        } else {
            if (jtfDescricao.getText().isEmpty()
                    || jdcPagamento.getDate().toString().isEmpty()
                    || jdcVencimento.getDate().toString().isEmpty()
                    || jtfValor.getText().isEmpty()
                    || (jcbStatus.getSelectedItem() != "Debitada" && jcbStatus.getSelectedItem() != "Não Debitada")) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatorios!");
            } else {
                facade.createDatas(despesa);
                JOptionPane.showMessageDialog(null, "Registro inserido com sucesso !");
                updateTable();
                enableComponents(false);
            }
        }
    }//GEN-LAST:event_jbtGravarActionPerformed


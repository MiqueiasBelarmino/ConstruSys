    private void jbtAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAdicionarActionPerformed
        if (jtClientes.isRowSelected(jtClientes.getSelectedRow())) {
            ClienteBuscaVisao.cliente = clienteList.get(jtClientes.getSelectedRow());
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Cliente!");
        }
    }//GEN-LAST:event_jbtAdicionarActionPerformed

    private void jbtFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtFiltrarActionPerformed
        String nome = jtfNome.getText();
        clienteList.clear();
        if (nome.isEmpty()) {
            clienteList.addAll(facade.findAll());
        } else {
            clienteList.addAll(facade.findByName(nome));
        }
    }//GEN-LAST:event_jbtFiltrarActionPerformed


    private void jbtGerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGerarActionPerformed
        if (jdcInicial.getDate().getTime() > jdcFinal.getDate().getTime()) {
            JOptionPane.showMessageDialog(null, "A data inicial deve ser menor que a final!", null, JOptionPane.WARNING_MESSAGE);
            jdcInicial.setDate(null);
        } else {

            vendaList = facade.searchByData(jdcInicial.getDate(), jdcFinal.getDate());
            vr = new VendasReport(vendaList);
        }

        jdcInicial.setDate(null);
        jdcFinal.setDate(null);
        jdcInicial.requestFocus();
    }//GEN-LAST:event_jbtGerarActionPerformed


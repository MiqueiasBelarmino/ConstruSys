    private void verifica(Pagamento paga) {
        if (paga.getPagas() == paga.getParcelas()) {
            paga.setStatus("pago");
            pagFacade.update(paga);
            venda = paga.getVenda();
            venda.setStatus("pago");
            vendaFac.update(venda);

        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jtPagamentos.isRowSelected(jtPagamentos.getSelectedRow())) {
            JOptionPane.showMessageDialog(null, "Selecione uma venda para pagar uma parcela");
        } else {
            pag = pagamentoList.get(jtPagamentos.getSelectedRow());
            if (pag.getPagas() < pag.getParcelas()) {
                pag.setPagas(pag.getPagas() + 1);
                pagFacade.update(pag);
                verifica(pag);
                pagamentoList.clear();
                pagamentoList.addAll(pagFacade.OrderByStatus());
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

